// Generates a `open-tag` wrapper script under ~/.open-tag/bin and injects it into the spawned agent's PATH.
// Agents call `open-tag ...` from their shell/bash tool → routed to this repo's CLI.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { binDir } from "../paths.js";

export function ensureOpenTagBin(): string {
  const dir = binDir();
  fs.mkdirSync(dir, { recursive: true });

  const here = path.dirname(fileURLToPath(import.meta.url)); // repo: .../src/daemon · bundled: .../dist
  // Bundled/npm mode (@open-tag/daemon): the agent CLI ships as a sibling bundle next to the daemon and
  // runs directly with node — there's no repo source or tsx on disk. Repo mode: run the TS source via tsx.
  const bundledCli = path.join(here, "agent-cli.mjs");
  let runner: string, cliPath: string;
  if (fs.existsSync(bundledCli)) {
    runner = "node";
    cliPath = bundledCli;
  } else {
    cliPath = path.resolve(here, "../cli/index.ts");
    const projectRoot = path.resolve(here, "../.."); // .../open-tag
    const tsxBin = path.join(projectRoot, "node_modules", ".bin", "tsx");
    runner = fs.existsSync(tsxBin) ? tsxBin : "npx tsx"; // prefer local tsx, fallback to npx
  }

  const q = (s: string) => JSON.stringify(s); // safe quoting for both sh and cmd.exe
  const wrapper = path.join(dir, "open-tag");
  const sh = `#!/bin/sh\n# open-tag agent CLI wrapper (auto-generated by daemon)\nexec ${q(runner)} ${q(cliPath)} "$@"\n`;
  fs.writeFileSync(wrapper, sh);
  fs.chmodSync(wrapper, 0o755);

  if (process.platform === "win32") {
    const cmdWrapper = path.join(dir, "open-tag.cmd");
    const cmdContent = `@echo off\r\nREM open-tag agent CLI wrapper (auto-generated by daemon)\r\n${q(runner)} ${q(cliPath)} %*\r\n`;
    fs.writeFileSync(cmdWrapper, cmdContent);
  }

  return dir;
}
